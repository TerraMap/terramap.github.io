function hexToRgb(hex: string) {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result ? {
    r: parseInt(result[2], 16),
    g: parseInt(result[3], 16),
    b: parseInt(result[4], 16)
  } : null;
}

